#ifndef OLED_SIT_SPEED
#    define OLED_SIT_SPEED 5
#endif
#ifndef OLED_SNEAK_SPEED
#    define OLED_SNEAK_SPEED 15
#endif
#ifndef OLED_WALK_SPEED
#    define OLED_WALK_SPEED 40
#endif

#if (OLED_BARK_FRAMES > OLED_ANIM_MAX_FRAMES) || (OLED_SIT_FRAMES > OLED_ANIM_MAX_FRAMES) || \
    (OLED_WALK_FRAMES > OLED_ANIM_MAX_FRAMES) || (OLED_SNEAK_FRAMES > OLED_ANIM_MAX_FRAMES) || \
    (OLED_RUN_FRAMES > OLED_ANIM_MAX_FRAMES)
#    error frame size too large
#endif

static uint8_t pet_animation_frame = 0;
static uint8_t pet_animation_type  = 0;

void render_pet(uint8_t col, uint8_t line) {
    render_animation(col, line, (const char (*)[OLED_ANIM_SIZE])pet_animiations[pet_animation_type], pet_animation_frame, OLED_PET_ROWS);
}

uint32_t pet_animation_phases(uint32_t triger_time, void *cb_arg) {
    static uint32_t anim_frame_duration = PET_FRAME_DURATION;
    if (host_keyboard_led_state().caps_lock) {
        pet_animation_type = 0;
        anim_frame_duration = update_animation_frame(&pet_animation_frame, OLED_BARK_FRAMES, PET_FRAME_DURATION);
    } else if (get_current_wpm() <= OLED_SIT_SPEED) {
        pet_animation_type = 1;
        anim_frame_duration = update_animation_frame(&pet_animation_frame, OLED_SIT_FRAMES, PET_FRAME_DURATION);
    } else if (get_current_wpm() <= OLED_SNEAK_SPEED) {
        pet_animation_type = 2;
        anim_frame_duration = update_animation_frame(&pet_animation_frame, OLED_SNEAK_FRAMES, PET_FRAME_DURATION);
    } else if (get_current_wpm() <= OLED_WALK_SPEED) {
        pet_animation_type = 3;
        anim_frame_duration = update_animation_frame(&pet_animation_frame, OLED_WALK_FRAMES, PET_FRAME_DURATION);
    } else {
        pet_animation_type = 4;
        anim_frame_duration = update_animation_frame(&pet_animation_frame, OLED_RUN_FRAMES, PET_FRAME_DURATION);
    }
    return anim_frame_duration;
}
