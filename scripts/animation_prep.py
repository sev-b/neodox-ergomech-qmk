import re

# a function to split a string into a list of strings that each hold 32 codes exactly
def split_string_into_32_word_chunks(input_string):
    # split the input string into a list of words
    words = re.findall(r"0x[0-9a-fA-F]+", input_string)
    # split the list of words into a list of lists of words, each containing 32 words
    chunks = [words[i:i + 32] for i in range(0, len(words), 32)]
    return chunks


# read the input string from the user
input_string = input()
# split the input string into a list of lists of words, each containing 32 words
chunks = split_string_into_32_word_chunks(input_string)

# a function to print the list of lists of words as in the example above with the curly braces and commas and newlines
def print_chunks(chunks):
    print("{")
    for chunk in chunks:
        print("    {", end="")
        for word in chunk:
            print(f" {word},", end="")
        print(" },")
    print("}")

# print the list of lists of words
print_chunks(chunks)
